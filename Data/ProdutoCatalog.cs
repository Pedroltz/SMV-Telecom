    public static Produto? FindBySlug(string slug) =>
        Todos.FirstOrDefault(p => p.Slug.Equals(slug, StringComparison.OrdinalIgnoreCase));

    public static List<Produto> GetRelacionados(Produto produto) =>
        produto.Relacionados
            .Select(FindBySlug)
            .Where(p => p is not null)
            .Cast<Produto>()
            .ToList();
